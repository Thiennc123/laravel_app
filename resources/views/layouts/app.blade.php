        $(document).ready(function() {

            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            src = "{{ route('searchProductAjax') }}";
            $("#search").autocomplete({
                source: function(request, response) {
                    $.ajax({
                        url: src,
                        data: {
                            term: request.term
                        },
                        dataType: "json",
                        success: function(data) {
                            response(data);
                        }
                    });
                },
                minLength: 1,
                maxShowItems: 5,
            });
            $(document).on('click', '.ui-menu-item', function() {
                $('#search-form').submit();
            });

            $('.countTotalProduct').focusout(function(e) {
                e.preventDefault();

                var thisClick = $(this);
                var count = $(this).val();
                var product_id = $(this).closest('.text-center').find('.product_id').val();
                var idBillStore = $('.idBillStore').val();
                var data = {
                    _token: $('input[name=_token]').val(),
                    count: count,
                    product_id: product_id,
                    idBillStore: idBillStore,
                };


                $.ajax({
                    url: '/updateCountOfBillStore',
                    type: 'POST',
                    data: data,
                    success: function(response) {

                        thisClick.closest('.thien').find('.total').text(response.count *
                            response.wPrice);

                        location.reload();



                    }
                });
            });

            $('.Save').click(function(e) {
                var idBill = $('.idBillStore').val();
                var status = 1;

                var data1 = {

                    status: status,
                };


                $.ajax({
                    url: '/updateBillStore/' + idBill,
                    type: 'POST',
                    data: data1,
                    success: function(response) {

                        window.location.href = "/updateProductAfterSave/" + idBill;



                    }
                });
            });

            $('.TSave').click(function(e) {
                var idBill = $('.idBillStore').val();
                var status = 0;

                var data1 = {


                    status: status,
                };


                $.ajax({
                    url: '/updateBillStore/' + idBill,
                    type: 'POST',
                    data: data1,
                    success: function(response) {

                        window.location.href = "/showBillStore";



                    }
                });
            });

            $('.remove').click(function(e) {
                e.preventDefault();
                var product_id = $(this).closest('.thien').find('.product_id')
                    .val();
                var idBillStore = $('.idBillStore').val();

                var data1 = {
                    idBillStore: idBillStore,
                };

                $.ajax({
                    url: '/removeBillStore/' + product_id,
                    //beforeSend: function(){
                    //    alert
